package EJ_OP_2;

public class Maleta 
{
	private boolean visible;
	private boolean metal;
	
	public Maleta(boolean visible, boolean metal)
	{
		this.metal = metal;
		this.visible = visible;
	}

	///////////////////////////////////////////
	public boolean isVisible() 
	{
		return visible;
	}
	///////////////////////////////////////////
	
	///////////////////////////////////////////
	public boolean isMetal() 
	{
		return metal;
	}
	///////////////////////////////////////////
	
	
}
